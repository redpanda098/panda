const day = "tuesday";

switch (day) {
    case "monday":
        console.log("It's Monday, start of the week!");
        break;
    case "tuesday":
        console.log("It's Tuesday, keep going!");
        break;
    case "wednesday":
        console.log("It's Wednesday, halfway through!");
        break;
    case "thursday":
        console.log("It's Thursday, almost there!");
        break;
    case "friday":
        console.log("It's Friday, the weekend is near!");
        break;
    case "saturday":
        console.log("It's Saturday, enjoy your weekend!");
        break;
    case "sunday":
        console.log("It's Sunday, rest and recharge!");
        break;
    default:
        console.log("Invalid day, please enter a valid day of the week.");  
        break;
}   